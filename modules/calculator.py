
def add(number_1, number_2):
    return f"The answer is {number_1 + number_2}"
    
def subtract(number_1, number_2):
    return f"The answer is {number_1 - number_2}"

def multiply(number_1, number_2):
    return f"The answer is {number_1 * number_2}"
    
def divide(number_1, number_2):
    return f"The answer is {int(number_1 / number_2)}"
